package com.napier.pr;
/**
 * Country class with basic information of a country
 *
 */
public class Country
{
    /**
     * Countries 3 letter code for example 'ABC'
     *
     */
    public String code;

    /**
     * Name of country
     *
     */
    public String name;
    /**
     *Continent name
     *
     */
    public String continent;
    /**
     * region name
     *
     */
    public String region;
    /**
     * population number
     *
     */
    public int population;

}
